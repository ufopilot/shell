

awk '  
    function bar(v){
        s=""; 
        c=34;               # greeen
        if (v>85) c=33      # yellow
        if (v>90) c=31      # red
        for (i=1; i<=v/4; i++){s=s"#"}
        for (j=i; j<=25; j++){s=s"."}
        return "\033["c"m"s"\033[0m"
    }
    
    NR==1{
        printf "\033[46m %-10s \033[0m|\033[46m %8s \033[0m|\033[46m %9s \033[0m|\033[46m %8s \033[0m|\033[46m %-32s \033[0m \033[46m %s \033[0m\n",
        $1,$2,$3,$4,$5,$6" "$7 
    }
    NR!=1{
        printf "\033[35m %-10s \033[0m|%10s |%10s |%10s |\033[36m %4s \033[0m [%-25s] \033[36m %s \033[0m\n",
        $1,$2,$3,$4, $5, bar($5),$6 

    }
' <(df $@)
